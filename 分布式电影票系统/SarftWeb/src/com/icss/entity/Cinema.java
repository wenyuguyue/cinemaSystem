package com.icss.entity;

public class Cinema {
	private int cid;
	private String code;
	private String name;
	private String corporation;
	private String contact;
	private String telephone;
	private String fax;
	private String cinemas;
	private int status;
	private int screen_count;

	public int getCid() {
		return cid;
	}

	public void setCid(int cid) {
		this.cid = cid;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getCorporation() {
		return corporation;
	}

	public void setCorporation(String corporation) {
		this.corporation = corporation;
	}

	public String getContact() {
		return contact;
	}

	public void setContact(String contact) {
		this.contact = contact;
	}

	public String getTelephone() {
		return telephone;
	}

	public void setTelephone(String telephone) {
		this.telephone = telephone;
	}

	public String getFax() {
		return fax;
	}

	public void setFax(String fax) {
		this.fax = fax;
	}

	public String getCinemas() {
		return cinemas;
	}

	public void setCinemas(String cinemas) {
		this.cinemas = cinemas;
	}

	public int getStatus() {
		return status;
	}

	public void setStatus(int status) {
		this.status = status;
	}

	public int getScreen_count() {
		return screen_count;
	}

	public void setScreen_count(int screen_count) {
		this.screen_count = screen_count;
	}

}
